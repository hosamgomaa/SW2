            'name' => $request->get('productName'),
            'description' => $request->get('productDescription'),
            'image' => $request->get('productImage'),
            'price' => $request->get('productPrice'),
            'status' => $request->get('productStatus'),
            'category_id' => $request->get('productCategory')
    
    
        ]);
        $product->save();
        return redirect('/add/product')->with('status', "{$product->name} Added!");
    }

